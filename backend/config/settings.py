def _normalize_database_url(url):
    parsed = urlsplit(url)
    filtered_query = [
        (key, value)
        for key, value in parse_qsl(parsed.query, keep_blank_values=True)
        if key.lower() != 'pgbouncer'
    ]
    return urlunsplit(parsed._replace(query=urlencode(filtered_query)))


